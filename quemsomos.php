<h1 style="padding-top: 90px;
    padding-bottom: 55px; font-size: 2.375rem;"><strong>A história do "Petrô"</strong></h1>
            <p style="text-align: justify !important;">O Petropolitano Foot-Ball Club foi fundado por um grupo de jovens no dia 04 de julho de 1911, no bar de um alemão chamado Max Meyer, no centro de Petrópolis. Até a década de 20, o clube não tinha sede e utilizava o campo de futebol do Terra Santa. No entanto, em 1923, após uma doação da família Guinle, o Petropolitano construiu sua Sede Esportiva no Valparaíso, com campo – Estádio Carlos Guinle – e duas quadras de tênis. Após muitos anos utilizando salões e teatros alugados, como o Cinema Petrópolis e o Cinema Dom Pedro, a Sede Social foi enfim inaugurada na década de 40, após a aquisição do Tênis Clube de Petrópolis.</p>  
