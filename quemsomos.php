<p style="text-align: justify !important;">Ao longo das décadas seguintes, o Clube foi crescendo e desenvolvendo seu espaço com a construção da piscina, dos grandes salões e da sauna. Em consonância com o crescimento físico, o Clube Petropolitano foi, cada vez mais, ganhando relevância perante o público. As Domingueiras com a Banda Kolling, os desfiles Bangu e o Baile de Máscaras, em parceria com a Prefeitura, movimentavam toda a cidade. No esporte, o Clube conquistava inúmeros títulos no futebol e em outras modalidades, como o Tricampeonato Estadual de Tênis de Campo e Octadecacampeonato de Basquete, isto é, o Clube foi campeão 18 vezes consecutivas.</p>  

<p style="text-align: justify !important">Em 1972, o Som Petrô, baile que marcou gerações e reuniu milhares de jovens aos domingos, foi criado. Quem não tem boas lembranças dessa festa?  Em 1980, o baile de pré-carnaval mais tradicional de Petrópolis surgiu: o Baile do Preto & Branco. O evento ainda ocorre todos os anos e é diversão garantida. Em nossos palcos, grandes nomes da música brasileira já fizeram a alegria do povo, como Gilberto Gil, Raul Seixas, Cazuza, Barão Vermelho, Ivete Sangalo e muito mais.</p> 

 
       
